    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("sid");
        EmployeeDAO dao = new EmployeeDAO();
        Employee ss = dao.getEmId(id);
        request.setAttribute("stt", ss);
        request.getRequestDispatcher("UpdateEmployee.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String sid = request.getParameter("id");

        String sfullname = request.getParameter("fullname");
        String sdob = request.getParameter("dob");
        String sgender = request.getParameter("gender");
        String saddress = request.getParameter("address");
        String sphonenumber = request.getParameter("phonenumber");
        String suser_id = request.getParameter("user_id");

        EmployeeDAO dao = new EmployeeDAO();
        dao.updateEmployee(sid, suser_id, sfullname, sdob, sgender, saddress, sphonenumber);
        response.sendRedirect("loadem");
    }

